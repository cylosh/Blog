                     <p>Hello there my fellow Dacians and/or interested earthlings! My life story began in august 1989 on the brink of revolution in <a href="https://en.wikipedia.org/wiki/Romanian_Revolution" style="font-weight:bold;" target="_blank">Romania</a> <span class="glyphicon glyphicon-new-window "></span>. While the political environment was changing in my country, so was everything related to technology as people knew it. That year the internet was getting ready to boom as CERN opened their first external TCP/IP connections and released the proposal for the <a href="http://info.cern.ch/hypertext/WWW/TheProject.html" style="font-weight:bold;" target="_blank">WWW</a> <span class="glyphicon glyphicon-new-window "></span> with all its basic concepts, the URL, http and html. Two months later RIPE forum(Réseaux IP Européens) was independently formed to maintain and develop the Internet and its unique characteristics. </p>
                     <p>Once that „link”( wink wink) or Pandora-box as it was called at the time by some, was released into the wild, a new form of revolution that was brewing for more than a decade within the hacker community: <a href="https://opensource.org/" style="font-weight:bold;" target="_blank">Open Source</a> <span class="glyphicon glyphicon-new-window "></span>, began to grow wings. Even though it officially started back in 1985 with the publication of the GNU manifesto, it could only get up its feet in January 1998 when Netscape made public the source for its Navigator which at the time was the dominant web browser. The move inspired the foundation of OSI( Open Source Initiative) the following month, an organization that continues to promote the free software concept even further.</p>
					 
					 <p>In 1991 the Linux kernel was released and once implemented into the GNU system, the first fully open source <a href="https://www.gnu.org/gnu/linux-and-gnu.html" style="font-weight:bold;" target="_blank">Operating System</a> <span class="glyphicon glyphicon-new-window "></span> was introduced to the world, things simply exploded.
                     It firstly proved a point to the growing community of programmers about free software and secondly for millions of newbies it was much easier to access computer programs and modify them to their needs. That created a worldwide user-base and a big expansion of everything related to programming. Therefore when I was only 5 years old and totally unsuspecting, the most popular scripting language used currently in a proportion of over 80% on the web - <a href="https://php.net/" style="font-weight:bold;" target="_blank">PHP</a> <span class="glyphicon glyphicon-new-window "></span> was created , a year later(1995) it was released to the public. Java was also released that year, along with other important tools like the Apache webserver.</p><p>By the time I started high school, the technology directly/adjacently used by computers was taking over the world. However, my folks started me on the traditional route in a mechatronics class at the Energetic College in Râmnicu Vâlcea which was great as it was the best available but once the computers bug reached me I moved at a Math-Info profile in pursuit of a programmer's career. Things went haywire as we were being learned the dead language of Pascal and after a misconduct or misstep of mine I was forced into becoming self-taught programmer and started studying any language related to the web. I became familiar with HTML and CSS, I script-kiddied myself into a bit of JavaScript and most importantly I learned the language of backend web development - PHP. I was active in our local <a href="http://www.phpromania.net/user/14927/oceans/activitate/" style="font-weight:bold;" target="_blank">Romanian PHP forum</a> <span class="glyphicon glyphicon-new-window "></span> and other places including Expert-Exchange where I reached the <a href="https://www.experts-exchange.com/members/al3cs12.html" style="font-weight:bold;" target="_blank">PHP Wizard</a> <span class="glyphicon glyphicon-new-window "></span> certificate with more than 400.000 points via contributions for PHP related questions.</p>
                     <p>In 2010 I drew a harsh and wrong conclusion that my passion shouldn't become my regular job, thus I left all of my programming projects of script-kiddies, helping others on forums and Experts sites behind and moved to Sibiu to help my brother with his local small-business. It took me several years to realize that the most successful people are the ones that follow their passion and thrive on their accomplishments hence I was back hacking( <a href="https://en.wikipedia.org/wiki/Hacker_culture" style="font-weight:bold;" target="_blank">this one</a> <span class="glyphicon glyphicon-new-window "></span> not the media brainwash clickbait) away during the summer of 2015. That however was a totally different world from which I've left it. PHP advanced to version 7, every PHP application was being written completely Object Oriented and software manufactures developed IDEs and tools to accomodate that, eg. Composer - released in 2012 for solving dependencies; there had been a major shift from CVS and SVN control versioning systems towards the distributed GIT created by the same Linus Torvalds, JavaScript was back with even more features following the release of 6th edition of ECMAScript and was also turned into the language for a server - NodeJS. Chrome with its very useful Developer Tools was released; Go language apparently wants to challenge C++ as the future <a href="https://en.wikipedia.org/wiki/System_programming_language#Mid-level_languages" style="font-weight:bold;" target="_blank">systems language</a> <span class="glyphicon glyphicon-new-window "></span> and basically there's a new programming language that comes out every day like hot pancakes. </p>
                     <p>I was soon on top of the latest challenges, went freelancing and within a year already completed couple dozen of projects for clients all over the world. I'm enthusiastic about creating quality apps and I love challenges. When I'm not doing that, I waste time trying to solve puzzles following Jumps equine races.</p>
